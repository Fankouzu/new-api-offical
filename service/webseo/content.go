func BuildBodyContent(meta Meta, requestURI string, baseURL string, pricings []model.Pricing, theme string) string {
	if meta.Robots != indexRobots {
		return ""
	}
	path := normalizePath(requestURI)
	base := normalizeBaseURL(baseURL)
	catalog := BuildCatalog(pricings)

	switch {
	case path == "/":
		return renderHomeBody(base, catalog, theme)
	case path == "/pricing":
		return renderPricingBody(base, catalog)
	case strings.HasPrefix(path, "/pricing/"):
		modelID, err := url.PathUnescape(strings.TrimPrefix(path, "/pricing/"))
		if err != nil {
			modelID = strings.TrimPrefix(path, "/pricing/")
		}
		if item, ok := findModel(catalog, modelID); ok {
			return renderModelBody(base, catalog, item)
		}
	case path == "/rankings" && theme != "classic":
		return renderRankingsBody(base, catalog)
	case path == "/about":
		return renderAboutBody(base)
	case topicPage(path) != nil:
		return renderTopicBody(base, catalog, *topicPage(path))
	}
	return ""
}

func renderHomeBody(base string, catalog []ModelSEOItem, theme string) string {
	var b strings.Builder
	startArticle(&b, "Lizh AI AI Model Marketplace")
	writeParagraph(&b, "Lizh AI is an AI model marketplace for developers who need OpenAI-compatible access to GPT, Gemini, DeepSeek, Qwen, Doubao, GLM, MiniMax, Kimi, and other mainstream models through one API gateway.")
	writeSection(&b, "Supported AI models", "Browse a compact catalog of text, image, multimodal, and tool-capable AI models. Every model page links to pricing, model IDs, capabilities, and integration notes.")
	writeModelLinks(&b, catalog, len(catalog))
	writeSection(&b, "Unified API and billing", "Use one account balance, one integration surface, and one model marketplace to compare costs and route workloads across providers. "+priceClarifierEN)
	writeSection(&b, "Developer entry points", "Start from model pricing, compare API costs, or read an OpenAI SDK compatibility guide before creating production keys.")
	links := []linkItem{
		{Text: "Model pricing marketplace", Href: "/pricing"},
		{Text: "About Lizh AI", Href: "/about"},
		{Text: "OpenAI-compatible API guide", Href: "/use-cases/openai-compatible-api"},
		{Text: "AI API pricing comparison", Href: "/compare/ai-api-pricing"},
	}
	if theme != "classic" {
		links = append(links, linkItem{Text: "Popular AI model rankings", Href: "/rankings"})
	}
	writeLinkList(&b, "Explore Lizh AI", links)
	endArticle(&b)
	return b.String()
}

