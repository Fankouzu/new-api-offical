func filterTopicModels(catalog []ModelSEOItem, path string) []ModelSEOItem {
	if strings.Contains(path, "gemini") {
		return filterFamily(catalog, "gemini")
	}
	if strings.Contains(path, "deepseek") {
		return filterFamily(catalog, "deepseek")
	}
	if strings.Contains(path, "qwen") {
		return filterFamily(catalog, "qwen")
	}
	return catalog
}

func filterFamily(catalog []ModelSEOItem, family string) []ModelSEOItem {
	filtered := make([]ModelSEOItem, 0)
	for _, item := range catalog {
		if strings.Contains(strings.ToLower(item.ID), family) || strings.Contains(strings.ToLower(item.Family), family) {
			filtered = append(filtered, item)
		}
	}
	if len(filtered) == 0 {
		return catalog
	}
	return filtered
}

func modelIntro(item ModelSEOItem) string {
	description := strings.TrimSpace(item.Description)
	if description != "" {
		return fmt.Sprintf("%s API is available in Lizh AI. %s", item.Name, description)
	}
	return fmt.Sprintf("%s API is part of the %s model family in the Lizh AI AI model marketplace.", item.Name, item.Family)
}

func modelScenarios(item ModelSEOItem) []string {
	scenarios := []string{"Use it for API workloads that need " + strings.Join(item.Capabilities, ", ") + "."}
	lower := strings.ToLower(item.ID + " " + item.Family)
	switch {
	case strings.Contains(lower, "deepseek"):
		scenarios = append(scenarios, "It is a good candidate for reasoning, coding, and cost-sensitive development workflows.")
	case strings.Contains(lower, "gemini"):
		scenarios = append(scenarios, "It is useful for multimodal and general-purpose application workflows.")
	case strings.Contains(lower, "qwen"):
		scenarios = append(scenarios, "It is useful for multilingual, coding, and general assistant workloads.")
	case strings.Contains(lower, "gpt"):
		scenarios = append(scenarios, "It is useful for general reasoning, coding, and production assistant experiences.")
	}
	return scenarios
}

type linkItem struct {
	Text string
	Href string
}

type faqItem struct {
	Question string
	Answer   string
}

func startArticle(builder *strings.Builder, h1 string) {
	builder.WriteString(`<article class="seo-content">`)
	builder.WriteString(`<h1>`)
	builder.WriteString(html.EscapeString(h1))
	builder.WriteString(`</h1>`)
}

func endArticle(builder *strings.Builder) {
	builder.WriteString(`</article>`)
}

func writeHeading(builder *strings.Builder, heading string) {
	builder.WriteString(`<h2>`)
	builder.WriteString(html.EscapeString(heading))
	builder.WriteString(`</h2>`)
}

func writeParagraph(builder *strings.Builder, text string) {
	builder.WriteString(`<p>`)
	builder.WriteString(html.EscapeString(text))
	builder.WriteString(`</p>`)
}

func writeSection(builder *strings.Builder, heading string, text string) {
	writeHeading(builder, heading)
	writeParagraph(builder, text)
}

func writeLinkList(builder *strings.Builder, heading string, links []linkItem) {
	writeHeading(builder, heading)
	builder.WriteString(`<ul>`)
	for _, link := range links {
		builder.WriteString(`<li><a href="`)
		builder.WriteString(html.EscapeString(link.Href))
		builder.WriteString(`">`)
		builder.WriteString(html.EscapeString(link.Text))
		builder.WriteString(`</a></li>`)
	}
	builder.WriteString(`</ul>`)
}

func writeModelLinks(builder *strings.Builder, catalog []ModelSEOItem, limit int) {
	builder.WriteString(`<ul>`)
	for idx, item := range catalog {
		if limit > 0 && idx >= limit {
			break
		}
		builder.WriteString(`<li><a href="`)
		builder.WriteString(html.EscapeString(modelURLPath(item.ID)))
		builder.WriteString(`">`)
		builder.WriteString(html.EscapeString(item.Name))
		builder.WriteString(` API pricing</a> - `)
		builder.WriteString(html.EscapeString(strings.Join(item.Capabilities, ", ")))
		builder.WriteString(`</li>`)
	}
	builder.WriteString(`</ul>`)
}

func writeModelTable(builder *strings.Builder, catalog []ModelSEOItem) {
	builder.WriteString(`<table><thead><tr><th>Model</th><th>Family</th><th>Input</th><th>Output</th><th>Capabilities</th></tr></thead><tbody>`)
	for _, item := range catalog {
		builder.WriteString(`<tr><td><a href="`)
		builder.WriteString(html.EscapeString(modelURLPath(item.ID)))
		builder.WriteString(`">`)
		builder.WriteString(html.EscapeString(item.Name))
		builder.WriteString(`</a></td><td>`)
		builder.WriteString(html.EscapeString(item.Family))
		builder.WriteString(`</td><td>约 `)
		builder.WriteString(html.EscapeString(formatPrice(item.InputPrice)))
		builder.WriteString(`</td><td>约 `)
		builder.WriteString(html.EscapeString(formatPrice(item.OutputPrice)))
		builder.WriteString(`</td><td>`)
		builder.WriteString(html.EscapeString(strings.Join(item.Capabilities, ", ")))
		builder.WriteString(`</td></tr>`)
	}
	builder.WriteString(`</tbody></table>`)
}

func writeTableRow(builder *strings.Builder, header string, value string) {
	builder.WriteString(`<tr><th>`)
	builder.WriteString(html.EscapeString(header))
	builder.WriteString(`</th><td>`)
	builder.WriteString(html.EscapeString(value))
	builder.WriteString(`</td></tr>`)
}

func writeFAQ(builder *strings.Builder, items []faqItem) {
	writeHeading(builder, "FAQ")
	for _, item := range items {
		builder.WriteString(`<section><h3>`)
		builder.WriteString(html.EscapeString(item.Question))
		builder.WriteString(`</h3><p>`)
		builder.WriteString(html.EscapeString(item.Answer))
		builder.WriteString(`</p></section>`)
	}
}

func relatedLinks(catalog []ModelSEOItem, current ModelSEOItem) []linkItem {
	links := []linkItem{{Text: "All AI model prices", Href: "/pricing"}}
	sameFamily := make([]ModelSEOItem, 0)
	for _, item := range catalog {
		if item.ID == current.ID {
			continue
		}
		if item.Family == current.Family {
			sameFamily = append(sameFamily, item)
		}
	}
	sort.Slice(sameFamily, func(i, j int) bool {
		return sameFamily[i].InputPrice < sameFamily[j].InputPrice
	})
	for _, item := range sameFamily {
		if len(links) >= 5 {
			break
		}
		links = append(links, linkItem{Text: item.Name + " API pricing", Href: modelURLPath(item.ID)})
	}
	for _, item := range catalog {
		if len(links) >= 5 {
			break
		}
		if item.ID != current.ID && item.Family != current.Family {
			links = append(links, linkItem{Text: item.Name + " API pricing", Href: modelURLPath(item.ID)})
		}
	}
	return links
}
