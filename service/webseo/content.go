type topicDefinition struct {
	Path        string
	H1          string
	Title       string
	Description string
	Intro       string
	Sections    []contentSection
	FAQ         []faqItem
	PrimaryLink string
}

type contentSection struct {
	Title string
	Body  string
}

func renderTopicBody(base string, catalog []ModelSEOItem, topic topicDefinition) string {
	var b strings.Builder
	startArticle(&b, topic.H1)
	writeParagraph(&b, topic.Intro)
	for _, section := range topic.Sections {
		writeSection(&b, section.Title, section.Body)
	}
	writeHeading(&b, "Related model pages")
	writeModelLinks(&b, filterTopicModels(catalog, topic.Path), len(catalog))
	links := []linkItem{{Text: "AI model pricing", Href: "/pricing"}}
	if topic.PrimaryLink != "" {
		links = append(links, linkItem{Text: "Related guide", Href: topic.PrimaryLink})
	}
	writeLinkList(&b, "Continue reading", links)
	writeFAQ(&b, topic.FAQ)
	endArticle(&b)
	_ = base
	return b.String()
}

func topicPage(path string) *topicDefinition {
	for _, topic := range topicPages() {
		if topic.Path == path {
			topicCopy := topic
			return &topicCopy
		}
	}
	return nil
}

